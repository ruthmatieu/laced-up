export class Button {
    constructor(
        border, 
        backgroundColor,
        fontSize,
        color,
        height,
        radius,
        width
    ) {
        this.border = border;
        this.backgroundColor = backgroundColor;
        this.fontSize = fontSize;
        this.color = color;
        this.height = height;
        this.radius = radius;
        this.width = width;
    }
}